package com.hp.hpl.CHAOS.Rewriting;

public class childQueryInfo extends QueryInfo {
	private int childID;
	private int nestedposition;
	// 1 represent positive
	private int positiveComponent;
	public int getChildID() {
		return childID;
	}
	public void setChildID(int childID) {
		this.childID = childID;
	}
	public int getNestedposition() {
		return nestedposition;
	}
	public void setNestedposition(int nestedposition) {
		this.nestedposition = nestedposition;
	}
	public int getPositiveComponent() {
		return positiveComponent;
	}
	public void setPositiveComponent(int positiveComponent) {
		this.positiveComponent = positiveComponent;
	}

	
	



}
